''' Faça um programa em Python que leia dois números inteiros e exiba
o quadrado da diferença do primeiro valor pelo segundo.'''

print('Digite um número inteiro: ')
n1 = int (input())
print('Digite outro número inteiro: ')
n2 = int (input())

operacao = (n1-n2)**2
print('A diferença elevada ao quadrado é: ',operacao)
